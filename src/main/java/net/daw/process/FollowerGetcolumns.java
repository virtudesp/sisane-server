package net.daw.process;

import com.google.gson.Gson;
import java.util.ArrayList;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import net.daw.dao.FollowerDao;
import net.daw.helper.Conexion;


/**
 *
 * @author rafa
 */
public class FollowerGetcolumns implements GenericOperation {

    @Override
    public String execute(HttpServletRequest request, HttpServletResponse response) throws Exception {
        ArrayList<String> alColumns = null;
        try {
            FollowerDao oFollowerDAO = new FollowerDao();
            alColumns = oFollowerDAO.getColumnsNames();
            String data = new Gson().toJson(alColumns);
            data = "{\"data\":" + data + "}";
            return data;
        } catch (Exception e) {
            throw new ServletException("FollowerGetcolumnsJson: View Error: " + e.getMessage());
        }
    }

}
